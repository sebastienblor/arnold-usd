/* Print OpenGL error messages */

void PrintGLError(const char *operation)
{
   GLenum error = glGetError();
   switch (error)
   {
      case GL_NO_ERROR:
         // everything fine!
         break;
      case GL_INVALID_ENUM:
         AiMsgWarning("[OpenGL] %s: invalid enum", operation);
         break;
      case GL_INVALID_VALUE:
         AiMsgWarning("[OpenGL] %s: invalid value", operation);
         break;
      case GL_INVALID_OPERATION:
         AiMsgWarning("[OpenGL] %s: invalid operation", operation);
         break;
      case GL_OUT_OF_MEMORY:
         AiMsgWarning("[OpenGL] %s: out of memory", operation);
         break;
      case GL_STACK_UNDERFLOW:
         AiMsgWarning("[OpenGL] %s: stack underflow", operation);
         break;
      case GL_STACK_OVERFLOW:
         AiMsgWarning("[OpenGL] %s: stack overflow", operation);
         break;
      default:
         AiMsgWarning("[OpenGL] %s: unrecognized error (%d)", operation, error);
   }
   return;
}



/* debugging aid */
static void PrintGPUVariable(const char *name, GLenum type)
{
   const unsigned char err_mark[4] = {0xff, 0xff, 0xff, 0xff};
   float value[32];
   int a;

   memset(value, 0xff, sizeof(value));
   glGetFloatv(type, value);

   std::cerr<<name<<" : ";

   for (a = 0; a < 32; a++) 
   {
      if (memcmp(&value[a], err_mark, sizeof(value[a])) == 0) break;
      std::cerr<<value[a]<<" ";
   }

   std::cerr<<std::endl;
}

