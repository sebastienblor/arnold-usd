struct Mesh{
    Bifrost::API::Array<amino::Math::vec3f> vertices;
    Bifrost::API::Array<amino::Math::vec3i> indices;
    unsigned long index; // cumulated index used in endTraverse to add triangles from other visitors
};

// marching cube every voxel
class MarchingCubesVisitor : public Bifrost::API::Visitor
{
public:
    static int c_edgeTable[256];
    static int c_triTable[256][16];
    static int c_cubeVerts[8][3];
    static int c_edgeIndex[12][2];

    Bifrost::API::VoxelChannel sdf;
    Bifrost::API::VoxelSampler sampler;

    int sampleRate;
    float invSampleRate;
    int tileWidth;
    float voxelScale;

    std::unordered_map<amino::Math::vec3f, size_t> ids;
    Bifrost::API::Array<amino::Math::vec3f> vertices;
    Bifrost::API::Array<amino::Math::vec3i> indices;

    std::vector<Mesh> meshes;
#ifdef PROCESSING_PROFILING
    std::chrono::high_resolution_clock::duration joining_time = std::chrono::high_resolution_clock::duration(0); // for profiling
#endif

    inline size_t getVertexCount() const{ return vertices.count(); }
    inline void addVertex(const amino::Math::vec3f& position) { vertices.add(position); }
    inline const amino::Math::vec3f& getVertex(unsigned int i) const{ assert(i < vertices.count()); return vertices[i]; }

    inline size_t getTriangleCount() const{ return indices.count(); }
    inline void addTriangle(const amino::Math::vec3i& triangle) { indices.add(triangle); }
    inline const amino::Math::vec3i& getTriangle(unsigned int i) const { assert(i < indices.count()); return indices[i]; }

    MarchingCubesVisitor(const Bifrost::API::VoxelChannel& sdf, unsigned int subdivisions)
        : sdf(sdf),
          sampler(sdf.createSampler(VOXEL_SAMPLER_TYPE, Bifrost::API::SamplerSpace::TileSpace)),
          sampleRate(subdivisions+1), invSampleRate(1./sampleRate),
          tileWidth(Bifrost::API::Layout(sdf.layout()).tileDimInfo().tileWidth),
          voxelScale(Bifrost::API::Layout(sdf.layout()).voxelScale()) {}

    MarchingCubesVisitor(const MarchingCubesVisitor& o)
        : sdf(o.sdf), sampler(sdf.createSampler(VOXEL_SAMPLER_TYPE, Bifrost::API::SamplerSpace::TileSpace)),
          sampleRate(o.sampleRate), invSampleRate(o.invSampleRate), tileWidth(o.tileWidth), voxelScale(o.voxelScale) {}

    Bifrost::API::Visitor* copy() const{ return new MarchingCubesVisitor(*this); }

    size_t calcVoxel(float *gridVals, const amino::Math::vec3i &voxelCornerPos);
    void beginTile(const Bifrost::API::TileAccessor& accessor, const Bifrost::API::TreeIndex& index);
    void join(const Bifrost::API::Visitor& visitor);
    void endTraverse(const Bifrost::API::TileAccessor &);
};

// voxelCornerPos is in scaled tile space (never use floats!)
size_t MarchingCubesVisitor::calcVoxel(float *gridVals, const amino::Math::vec3i &voxelCornerPos){
    // define vars
    int edges[12];
    int cubeIndex = 0;

    for ( int corner = 0; corner < 8; corner++ ) {
        cubeIndex |= ( gridVals[corner] > 0.0f ) ? 1 << corner : 0;
    }

    // compute vertices
    if ( c_edgeTable[cubeIndex] == 0 ) {
        return 0;
    }

    size_t vertexCount = 0;
    for ( int count = 0; count < 12; count++ ) {
        if ( ( c_edgeTable[cubeIndex] & ( 1 << count) ) == 0 ) {
            continue;
        }

        // calc a new vertex
        amino::Math::vec3f newVertex (0, 0, 0);
        amino::Math::vec2i e ( c_edgeIndex[count][0], c_edgeIndex[count][1] );

        amino::Math::vec3i p0 ( c_cubeVerts[e[0]] );
        amino::Math::vec3i p1 ( c_cubeVerts[e[1]] );

        float a = gridVals[ e[0] ];
        float b = gridVals[ e[1] ];
        float d = a - b;

        float t = (std::fabs(d)>FLT_EPSILON)? a/d : 0.0f;

        for ( int coord = 0; coord < 3; coord++ ) {
            // calc interpolated vertex position in tile space
            newVertex[ coord ] = (voxelCornerPos[coord] + p0[coord]) + t * ( p1[coord] - p0[coord] );
            newVertex[ coord ] *= invSampleRate * voxelScale;
        }

        // check whether this point is generated before
        auto positionInserted = ids.find( newVertex );
        if ( positionInserted != ids.end() ) {
            edges[count] = (int) positionInserted->second;
        } else {
            size_t vertexID = getVertexCount();
            edges[count] = (int) vertexID;
            addVertex( newVertex );
            ids[newVertex] = vertexID;
        }
        vertexCount += 1;
    }

    for ( int count = 0; c_triTable[cubeIndex][count]!=-1; count+= 3 ) {
        amino::Math::vec3i triangle(edges[c_triTable[cubeIndex][count]],
                                    edges[c_triTable[cubeIndex][count + 1]],
                                    edges[c_triTable[cubeIndex][count + 2]]);
        addTriangle(triangle);
    }
    return vertexCount;
}

void MarchingCubesVisitor::beginTile(const Bifrost::API::TileAccessor& accessor, const Bifrost::API::TreeIndex& index)
{
    // get the tile and its coordinate
    const Bifrost::API::Tile tile = accessor.tile(index);
    const Bifrost::API::TileCoord coord	= tile.coord();
    const Bifrost::API::TileData<float> data = sdf.tileData<float>(index);

    // now for each voxel in the tile, do marching cube calc
    size_t vertexCount = 0;
    FOR_IJK(i,j,k,tileWidth){
        if(fabs(data(i,j,k)) > 1) continue; // doesn't contain a surface, early out

        Bifrost::API::TileCoord scaledCoord((coord.i+i)*sampleRate, (coord.j+j)*sampleRate, (coord.k+k)*sampleRate);
        FOR_IJK(ii,jj,kk,sampleRate){
            float gridVals[8];

            // corner in scaled tile space
            amino::Math::vec3i cornerPos(scaledCoord.i + ii, scaledCoord.j + jj, scaledCoord.k + kk);

            // get grid values at the corners of this voxel
            for ( size_t e = 0; e < 8; ++e ) {
                // operate in scaled tile space here
                amino::Math::vec3i corner(c_cubeVerts[e][0], c_cubeVerts[e][1], c_cubeVerts[e][2] );

                // calc voxel pos in scaled tile space
                amino::Math::vec3i voxelPos = cornerPos + corner;
                amino::Math::vec3f tileSpacePos((float)voxelPos[0]*invSampleRate, (float)voxelPos[1]*invSampleRate, (float)voxelPos[2]*invSampleRate);

                gridVals[e] = sampler.sample<float>(tileSpacePos);
            }

            // run voxel mesher
            vertexCount += calcVoxel( gridVals, cornerPos );
        }
    }
}

void MarchingCubesVisitor::join(const Bifrost::API::Visitor& visitor)
{
#ifdef PROCESSING_PROFILING
    std::chrono::high_resolution_clock::time_point start(std::chrono::high_resolution_clock::now());
#endif
    const MarchingCubesVisitor& o = static_cast<const MarchingCubesVisitor&>(visitor);
    // only do final merge in endTraverse to avoid needless copies of huge arrays (vertices/indices)
    unsigned long index = meshes.size()==0? indices.count() : meshes[meshes.size()-1].index + meshes[meshes.size()-1].indices.count();
    for(const Mesh& mesh : o.meshes){
        meshes.push_back({ mesh.vertices, mesh.indices, index });
        index += mesh.indices.count();
    }
    if(o.indices.count() > 0){
        meshes.push_back({ o.vertices, o.indices, index });
    }
#ifdef PROCESSING_PROFILING
    joining_time += o.joining_time + (std::chrono::high_resolution_clock::now() - start);
#endif
}

void MarchingCubesVisitor::endTraverse(const Bifrost::API::TileAccessor &){
#ifdef PROCESSING_PROFILING
    Bifrost::Private::Profiler::dump("MARCHING CUBES JOIN", joining_time);
#endif
    {
        PROFILER("MARCHING CUBES MERGE VERTICES");
        for(const Mesh& mesh : meshes){
            for(unsigned int i = 0; i < mesh.vertices.count(); ++i){
                const amino::Math::vec3f& newVertex = mesh.vertices[i];
                if(ids.find(newVertex) == ids.end()){ // only add new vertices
                    size_t vertexID = getVertexCount();
                    addVertex( newVertex );
                    ids[newVertex] = vertexID;
                }
            }
        }
    }
    {
        PROFILER("MARCHING CUBES MERGE TRIANGLES");
        unsigned long total = (meshes.size()==0? 0 : meshes[meshes.size()-1].index+meshes[meshes.size()-1].index);
        indices.resize(total);
        Bifrost::Private::TBB_FOR_ALL(0, meshes.size(), 1, [&](size_t i){
            const Mesh& mesh = meshes[i];
            for(unsigned int j = 0; j < mesh.indices.count(); ++j){ // remap triangles
                const amino::Math::vec3i& triangle = mesh.indices[j];
                indices[mesh.index+j] = amino::Math::vec3i(ids[mesh.vertices[triangle[0]]], ids[mesh.vertices[triangle[1]]], ids[mesh.vertices[triangle[2]]]);
            }
        });
    }
}

